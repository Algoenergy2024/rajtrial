// Helper functions
export const getEUCitiesByCountry = (country) => euCities.filter(c => c.country === country);
export const getEUCountries = () => [...new Set(euCities.map(c => c.country))].sort();
export const getEUCityById = (id) => euCities.find(c => c.id === id);
export const getCitiesWithEmissions = () => euCities.filter(c => c.emissions.total !== null);

// Get total population
export const getTotalEUPopulation = () => euCities.reduce((sum, c) => sum + (c.population || 0), 0);

// Get cities by population range
export const getLargeCities = () => euCities.filter(c => c.population >= 500000);
export const getMediumCities = () => euCities.filter(c => c.population >= 200000 && c.population < 500000);
export const getSmallCities = () => euCities.filter(c => c.population < 200000);
